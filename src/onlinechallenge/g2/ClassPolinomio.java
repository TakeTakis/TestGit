
package onlinechallenge.g2;


public class ClassPolinomio {
    private int parteNumeral;
    private int parteAlgebraica;
     

    public ClassPolinomio(int parteNumeral, int parteAlgebraica) {
        this.parteNumeral = parteNumeral;
        this.parteAlgebraica = parteAlgebraica;
    }

    public int getParteNumeral() {
        return parteNumeral;
    }

    public int getParteAlgebraica() {
        return parteAlgebraica;
    }

}
